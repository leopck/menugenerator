	public Emulator() {
		setPreferredSize(new Dimension(260,200));
		setBackground(Color.DARK_GRAY);
		setText(Menu.active.startBlock.header);
	}
	
	public void setText(String text) {
		this.text = text;
		repaint();
	}
	
	@Override
	protected void paintComponent(Graphics gr) {
		super.paintComponent(gr);
		
		Graphics2D g = (Graphics2D) gr.create();
		g.setRenderingHint(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);
		Image img;
		
		try {
			img = ImageIO.read(new File("img/lcd_2x20_348.jpg"));
		} catch (IOException e) {
			e.printStackTrace();
			return;
		}
		
		double ratio = (double)img.getWidth(null) / (double)img.getHeight(null);
		int img_width = this.getWidth();
		double lcd_w = (int)(img_width*0.82);
		double lcd_h = (int)(img_width/ratio*0.5);
		
		g.drawImage(img,0,0, img_width, (int)(img_width/ratio),null);
		g.translate(23, 20);
		g.scale(lcd_w / (cols*3*6), lcd_h / (rows*3*8));
		
		int col = 0;
		int row = 0;
		for (int i = 0; i < text.length(); i++) {
			if (text.toCharArray()[i] == '\n') {
				row++;
				col = 0;
			}
			else {
				for (int y = 0; y < 8; y++) {
					for (int x = 0; x < 5; x++) {
						if (((font[text.toCharArray()[i]-32][x] >> (y)) & 1) > 0 && col < cols)
							g.fillRect(x*3 + col*6*3, y*3 + row * 3*8, 2, 2);
					}
				}				
				col++;
			}
		}
		
		g.dispose();
	}
